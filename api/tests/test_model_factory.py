"""Tests for the GlobalModel factory functions.

Verifies that _make_simple_global_model and _make_compose_global_models
generate classes with the correct fields, constraints, and helpers — without
requiring DB access.  Migration-compatibility of the three converted simple
globals (Location, ClayBody, GlazeMethod) is validated by running
`manage.py makemigrations --check` in CI.
"""
import pytest
from django.db import models


# ---------------------------------------------------------------------------
# _dsl_field_to_django_field
# ---------------------------------------------------------------------------

class TestDslFieldToDjangoField:
    """Unit tests for the DSL field → Django field mapping helper."""

    def _call(self, field_name: str, field_def: dict):
        from api.models import _dsl_field_to_django_field
        return _dsl_field_to_django_field(field_name, field_def)

    def test_name_field_is_char255_no_blank(self):
        f = self._call('name', {'type': 'string'})
        assert isinstance(f, models.CharField)
        assert f.max_length == 255
        assert not f.blank

    def test_string_field_is_char1024_blank(self):
        f = self._call('short_description', {'type': 'string'})
        assert isinstance(f, models.CharField)
        assert f.max_length == 1024
        assert f.blank

    def test_image_field_is_char1024_blank(self):
        f = self._call('test_tile_image', {'type': 'image'})
        assert isinstance(f, models.CharField)
        assert f.max_length == 1024
        assert f.blank

    def test_enum_field_uses_choices_and_capped_max_length(self):
        f = self._call('cone', {'type': 'string', 'enum': ['04', '6', '10']})
        assert isinstance(f, models.CharField)
        assert f.max_length == 16   # max(2, 16)
        assert f.choices == [('04', '04'), ('6', '6'), ('10', '10')]

    def test_integer_field(self):
        f = self._call('temperature_c', {'type': 'integer'})
        assert isinstance(f, models.IntegerField)
        assert f.null and f.blank

    def test_number_field(self):
        f = self._call('weight_grams', {'type': 'number'})
        assert isinstance(f, models.FloatField)
        assert f.null and f.blank

    def test_boolean_field_is_nullable(self):
        f = self._call('is_food_safe', {'type': 'boolean'})
        assert isinstance(f, models.BooleanField)
        assert f.null and f.blank

    def test_unknown_type_falls_back_to_json(self):
        f = self._call('data', {'type': 'object'})
        assert isinstance(f, models.JSONField)

    def test_max_length_overrides_default(self):
        f = self._call('atmosphere', {'type': 'string', 'max_length': 255})
        assert isinstance(f, models.CharField)
        assert f.max_length == 255


