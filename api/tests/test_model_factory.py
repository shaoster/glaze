class TestMakeSimpleGlobalModel:
    """Verify the structure of classes generated by _make_simple_global_model."""

    @pytest.fixture
    def make(self, monkeypatch):
        """Return the factory, with _GLOBALS_MAP replaced by a minimal fixture."""
        import api.workflow as wf
        import api.models as m

        fake_map = {
            'widget': {
                'model': 'Widget',
                'public': False,
                'private': True,
                'fields': {
                    'name': {'type': 'string'},
                    'color': {'type': 'string'},
                    'weight': {'type': 'number'},
                },
            },
            'gadget': {
                'model': 'Gadget',
                'public': True,
                'private': True,
                'fields': {
                    'name': {'type': 'string'},
                    'serial': {'type': 'string'},
                },
            },
            'gizmo': {
                'model': 'Gizmo',
                'public': True,
                'private': False,
                'fields': {
                    'name': {'type': 'string'},
                },
            },
        }
        monkeypatch.setattr(wf, '_GLOBALS_MAP', fake_map)
        return m._make_simple_global_model

    def test_class_name_matches_model_key(self, make):
        Widget = make('widget')
        assert Widget.__name__ == 'Widget'

    def test_module_is_api_models(self, make):
        Widget = make('widget')
        assert Widget.__module__ == 'api.models'

    def test_inherits_from_global_model(self, make):
        from api.models import GlobalModel
        Widget = make('widget')
        assert issubclass(Widget, GlobalModel)

    def test_private_only_user_fk_is_not_nullable(self, make):
        Widget = make('widget')
        user_field = Widget._meta.get_field('user')
        assert not user_field.null

    def test_public_private_user_fk_is_nullable(self, make):
        Gadget = make('gadget')
        user_field = Gadget._meta.get_field('user')
        assert user_field.null

    def test_public_only_user_fk_is_nullable(self, make):
        Gizmo = make('gizmo')
        user_field = Gizmo._meta.get_field('user')
        assert user_field.null

    def test_inline_fields_are_present(self, make):
        Widget = make('widget')
        field_names = {f.name for f in Widget._meta.get_fields()}
        assert 'name' in field_names
        assert 'color' in field_names
        assert 'weight' in field_names

    def test_ref_fields_are_skipped(self, monkeypatch):
        """$ref fields in a global's fields section are additional_fields references
        and must not become model columns."""
        import api.workflow as wf
        import api.models as m
        monkeypatch.setattr(wf, '_GLOBALS_MAP', {
            'thing': {
                'model': 'Thing',
                'public': False,
                'private': True,
                'fields': {
                    'name': {'type': 'string'},
                    'clay_body': {'$ref': '@clay_body.name'},
                },
            },
        })
        Thing = m._make_simple_global_model('thing')
        field_names = {f.name for f in Thing._meta.get_fields()}
        assert 'name' in field_names
        assert 'clay_body' not in field_names

    def test_related_name_pluralizes_global_name(self, make):
        Widget = make('widget')
        user_field = Widget._meta.get_field('user')
        assert user_field.remote_field.get_accessor_name() == 'widgets'

    def test_private_only_has_one_constraint(self, make):
        Widget = make('widget')
        assert len(Widget._meta.constraints) == 1
        c = Widget._meta.constraints[0]
        assert c.name == 'uniq_widget_name_per_user'
        assert list(c.fields) == ['user', 'name']
        assert c.condition is None

    def test_public_private_has_two_constraints(self, make):
        Gadget = make('gadget')
        names = {c.name for c in Gadget._meta.constraints}
        assert names == {'uniq_gadget_name_per_user', 'uniq_gadget_name_public'}

    def test_public_only_has_one_public_constraint(self, make):
        Gizmo = make('gizmo')
        assert len(Gizmo._meta.constraints) == 1
        assert Gizmo._meta.constraints[0].name == 'uniq_gizmo_name_public'

    def test_str_returns_name(self, make):
        Widget = make('widget')
        instance = Widget.__new__(Widget)
        instance.name = 'My Widget'
        assert str(instance) == 'My Widget'

    def test_related_name_uses_plural_key_when_present(self, monkeypatch):
        """When the global config carries a 'plural' key the factory must use it
        for the user FK related_name instead of auto-pluralizing the global name."""
        import api.workflow as wf
        import api.models as m
        monkeypatch.setattr(wf, '_GLOBALS_MAP', {
            'entity': {
                'model': 'Entity',
                'plural': 'entities',
                'public': False,
                'private': True,
                'fields': {'name': {'type': 'string'}},
            },
        })
        Entity = m._make_simple_global_model('entity')
        user_field = Entity._meta.get_field('user')
        assert user_field.remote_field.get_accessor_name() == 'entities'

    def test_unknown_global_raises(self, make):
        with pytest.raises(ValueError, match='Unknown global'):
            make('nonexistent')


