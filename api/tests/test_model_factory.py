class TestConvertedSimpleGlobals:
    """Smoke-test the three existing globals that were converted to factory output.

    Migration compatibility is verified by `manage.py makemigrations --check`
    in CI; these tests guard the Python-level structure.
    """

    @pytest.mark.parametrize('model_name,global_name', [
        ('Location', 'location'),
        ('ClayBody', 'clay_body'),
        ('GlazeMethod', 'glaze_method'),
    ])
    def test_model_registered_in_workflow_globals(self, model_name, global_name):
        from api.workflow import get_global_config
        config = get_global_config(global_name)
        assert config['model'] == model_name

    @pytest.mark.parametrize('cls_name', ['Location', 'ClayBody', 'GlazeMethod'])
    def test_model_in_global_registry(self, cls_name):
        from api.models import GlobalModel
        names = {c.__name__ for c in GlobalModel._registry}
        assert cls_name in names

    def test_location_user_fk_is_not_nullable(self):
        from api.models import Location
        assert not Location._meta.get_field('user').null

    def test_clay_body_user_fk_is_nullable(self):
        from api.models import ClayBody
        assert ClayBody._meta.get_field('user').null

    def test_glaze_method_has_short_description(self):
        from api.models import GlazeMethod
        field = GlazeMethod._meta.get_field('short_description')
        assert isinstance(field, models.CharField)
        assert field.max_length == 1024
